  /// Get products by category
  static List<Product> getByCategory(String category) {
    if (category == 'All') return products;
    return products.where((p) => p.category == category).toList();
  }

  /// Get featured products (highest rated)
  static List<Product> getFeatured({int limit = 6}) {
    final sorted = [...products]..sort((a, b) => b.rating.compareTo(a.rating));
    return sorted.take(limit).toList();
  }

  /// Search products by name or description
  static List<Product> search(String query) {
    final lowercaseQuery = query.toLowerCase();
    return products.where((p) {
      return p.name.toLowerCase().contains(lowercaseQuery) ||
          (p.description?.toLowerCase().contains(lowercaseQuery) ?? false);
    }).toList();
  }
